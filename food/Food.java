package food;
abstract public class Food {
    private int energyValue;
    private int pleasureOfEating;

    public int getEnergyValue() {
        return energyValue;
    }

    public int getPleasureOfEating() {
        return pleasureOfEating;
    }

    public void setEnergyValue(int energyValue) {
        this.energyValue = energyValue;
    }

    public void setPleasureOfEating(int pleasureOfEating) {
        this.pleasureOfEating = pleasureOfEating;
    }
}











